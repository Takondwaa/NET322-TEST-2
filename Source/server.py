# REST handlers
# ═══════════════════════════════════════════════════════════════════

async def get_sensors(request: web.Request) -> web.Response:
    """GET /sensors — list all registered sensors."""
    media = negotiate(request)
    try:
        sensors = await storage.get_sensors()
    except Exception as exc:
        log.error("Storage error in get_sensors: %s", exc)
        return error_response(503, "storage unavailable", str(exc))

    resp = serialise(sensors, media, root="sensors")
    _ensure_session(request, resp)
    return resp


async def get_sensor(request: web.Request) -> web.Response:
    """GET /sensors/{id} — get one sensor by ID."""
    sensor_id = request.match_info["id"]
    media = negotiate(request)
    try:
        sensor = await storage.get_sensor(sensor_id)
    except Exception as exc:
        log.error("Storage error in get_sensor: %s", exc)
        return error_response(503, "storage unavailable", str(exc))

    if sensor is None:
        return error_response(404, "sensor not found", f"id={sensor_id}")

    resp = serialise(sensor, media, root="sensor")
    _ensure_session(request, resp)
    return resp


async def post_sensor(request: web.Request) -> web.Response:
    """POST /sensors — register a new sensor."""
    try:
        body = await request.json()
    except Exception:
        return error_response(400, "invalid JSON body")

    sensor_id    = body.get("id", "").strip()
    location     = body.get("location", "").strip()
    sensor_type  = body.get("sensor_type", "").strip()
    interval_s   = int(body.get("interval_s", 10))

    if not sensor_id or not location or not sensor_type:
        return error_response(400, "missing required fields", "id, location, sensor_type")

    try:
        if await storage.sensor_exists(sensor_id):
            return error_response(409, "sensor already registered", f"id={sensor_id}")
        sensor = await storage.register_sensor(sensor_id, location, sensor_type, interval_s)
    except Exception as exc:
        log.error("Storage error in post_sensor: %s", exc)
        return error_response(503, "storage unavailable", str(exc))

    media = negotiate(request)
    resp = serialise(sensor, media, root="sensor")
    resp.set_status(201)
    _ensure_session(request, resp)
    return resp


async def delete_sensor(request: web.Request) -> web.Response:
    """DELETE /sensors/{id} — remove a sensor and its readings."""
    sensor_id = request.match_info["id"]
    try:
        deleted = await storage.delete_sensor(sensor_id)
    except Exception as exc:
        log.error("Storage error in delete_sensor: %s", exc)
        return error_response(503, "storage unavailable", str(exc))

    if not deleted:
        return error_response(404, "sensor not found", f"id={sensor_id}")

    resp = web.Response(status=204)
    _ensure_session(request, resp)
    return resp


async def get_readings(request: web.Request) -> web.Response:
    """GET /sensors/{id}/readings?from=<ms>&to=<ms>"""
    sensor_id = request.match_info["id"]
    media = negotiate(request)

    # Parse optional time-range params
    from_ts = to_ts = None
    try:
        if "from" in request.rel_url.query:
            from_ts = int(request.rel_url.query["from"])
        if "to" in request.rel_url.query:
            to_ts = int(request.rel_url.query["to"])
    except ValueError:
        return error_response(400, "invalid query params", "'from' and 'to' must be Unix ms integers")

    try:
        if not await storage.sensor_exists(sensor_id):
            return error_response(404, "sensor not found", f"id={sensor_id}")
        readings = await storage.get_readings(sensor_id, from_ts, to_ts)
    except Exception as exc:
        log.error("Storage error in get_readings: %s", exc)
        return error_response(503, "storage unavailable", str(exc))

    resp = serialise(readings, media, root="readings")
    _ensure_session(request, resp)
    return resp


# ═══════════════════════════════════════════════════════════════════
# WebSocket handler
# ═══════════════════════════════════════════════════════════════════

async def websocket_handler(request: web.Request) -> web.WebSocketResponse:
    """
    GET /live — WebSocket live feed.

    The server pushes every incoming sensor reading as a JSON object.
    Clients may optionally send a JSON filter:
        {"sensor_id": "gh_a_temp_01"}
    to receive readings only from that sensor.
    """
    ws = web.WebSocketResponse(heartbeat=30)
    await ws.prepare(request)
    log.info("WebSocket client connected from %s", request.remote)

    q = broadcaster.register()
    sensor_filter: str | None = None

    try:
        # Run reader and writer tasks concurrently.
        async def _writer():
            while True:
                reading = await q.get()
                if sensor_filter and reading["sensor_id"] != sensor_filter:
                    continue
                try:
                    await ws.send_json(reading)
                except Exception:
                    break

        async def _reader():
            nonlocal sensor_filter
            async for msg in ws:
                if msg.type == web.WSMsgType.TEXT:
                    try:
                        data = json.loads(msg.data)
                        if "sensor_id" in data:
                            sensor_filter = data["sensor_id"]
                            log.debug("WS client filtered to sensor_id=%s", sensor_filter)
                    except json.JSONDecodeError:
                        pass
                elif msg.type in (web.WSMsgType.ERROR, web.WSMsgType.CLOSE):
                    break

        writer_task = asyncio.create_task(_writer())
        await _reader()
        writer_task.cancel()
        try:
            await writer_task
        except asyncio.CancelledError:
            pass

    finally:
        broadcaster.unregister(q)
        log.info("WebSocket client disconnected from %s", request.remote)

    return ws


