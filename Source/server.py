# ═══════════════════════════════════════════════════════════════════
# Application factory & entry point
# ═══════════════════════════════════════════════════════════════════

def make_app() -> web.Application:
    app = web.Application()
    app.router.add_get("/",                          dashboard)
    app.router.add_get("/sensors",                   get_sensors)
    app.router.add_get("/sensors/{id}",              get_sensor)
    app.router.add_post("/sensors",                  post_sensor)
    app.router.add_delete("/sensors/{id}",           delete_sensor)
    app.router.add_get("/sensors/{id}/readings",     get_readings)
    app.router.add_get("/live",                      websocket_handler)
    return app


async def main(tcp_port: int, http_port: int, db_path: str) -> None:
    # Open storage
    await storage.connect()
    log.info("Database ready at %s", db_path)

    # Start TCP sensor listener
    tcp_server = await asyncio.start_server(
        handle_sensor_connection, "0.0.0.0", tcp_port
    )
    log.info("TCP sensor listener on port %d", tcp_port)

    # Start HTTP/WebSocket server
    app = make_app()
    runner = web.AppRunner(app)
    await runner.setup()
    site = web.TCPSite(runner, "0.0.0.0", http_port)
    await site.start()
    log.info("HTTP/WebSocket server on http://0.0.0.0:%d", http_port)
    log.info("Dashboard: http://localhost:%d/", http_port)

    try:
        async with tcp_server:
            await asyncio.Event().wait()          # run forever
    except asyncio.CancelledError:
        pass
    finally:
        await runner.cleanup()
        await storage.close()
        log.info("Server shut down")


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="IoT Telemetry Server")
    parser.add_argument("--tcp-port",  type=int, default=9000)
    parser.add_argument("--http-port", type=int, default=8080)
    parser.add_argument("--db",        type=str, default="sensors.db")
    args = parser.parse_args()

    try:
        asyncio.run(main(args.tcp_port, args.http_port, args.db))
    except KeyboardInterrupt:
        log.info("Interrupted — bye")
