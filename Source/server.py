"""
server.py — IoT Telemetry Server

Starts two services on the same asyncio event loop:

  1. TCP listener (port 9000)  — accepts Protobuf-encoded sensor readings.
  2. HTTP/WebSocket server (port 8080) — REST API + live WebSocket feed.

Usage:
    python server.py [--tcp-port 9000] [--http-port 8080] [--db sensors.db]

All network code uses asyncio — no threads, no multiprocessing.
"""

import asyncio
import argparse
import json
import logging
import secrets
import struct
import sys
import time

from aiohttp import web
import aiosqlite

# Local modules
sys.path.insert(0, "proto")          # so we can import telemetry_pb2
import telemetry_pb2                 # noqa: E402  (generated by protoc)
from storage import Storage          # noqa: E402
from broadcaster import Broadcaster  # noqa: E402
from negotiation import negotiate, serialise, error_response  # noqa: E402

logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s  %(levelname)-8s %(name)s  %(message)s",
    datefmt="%H:%M:%S",
)
log = logging.getLogger("server")

# ── Shared application state ─────────────────────────────────────
storage = Storage()
broadcaster = Broadcaster()

# ═══════════════════════════════════════════════════════════════════
# TCP — Sensor ingestion
# ═══════════════════════════════════════════════════════════════════

async def handle_sensor_connection(
    reader: asyncio.StreamReader, writer: asyncio.StreamWriter
) -> None:
    """
    Coroutine spawned by asyncio.start_server() for each sensor TCP connection.

    Wire format: 4-byte big-endian length prefix followed by a serialised
    telemetry_pb2.Reading message.  The sensor sends one frame per reading.

    Failure modes handled:
      - ConnectionResetError / IncompleteReadError  → clean disconnect
      - DecodeError  → skip frame, log warning; close after 5 consecutive errors
    """
    addr = writer.get_extra_info("peername")
    log.info("Sensor connected from %s", addr)
    consecutive_errors = 0

    try:
        while True:
            # Read 4-byte length prefix
            raw_len = await asyncio.wait_for(reader.readexactly(4), timeout=60)
            (msg_len,) = struct.unpack(">I", raw_len)

            if msg_len == 0 or msg_len > 1_000_000:
                log.warning("Implausible frame length %d from %s — closing", msg_len, addr)
                break

            raw_msg = await asyncio.wait_for(reader.readexactly(msg_len), timeout=30)

            # Try SensorInfo first (sent once on connect for auto-registration)
            info = telemetry_pb2.SensorInfo()
            try:
                info.ParseFromString(raw_msg)
                if info.id:
                    if not await storage.sensor_exists(info.id):
                        await storage.register_sensor(
                            info.id, info.location,
                            info.sensor_type, info.interval_s or 10
                        )
                        log.info("Auto-registered sensor '%s' from %s", info.id, addr)
                    consecutive_errors = 0
                    continue
            except Exception:
                pass  # not a SensorInfo — fall through to Reading

            # Deserialise as a Reading
            reading = telemetry_pb2.Reading()
            try:
                reading.ParseFromString(raw_msg)
                consecutive_errors = 0
            except Exception as exc:
                consecutive_errors += 1
                log.warning("DecodeError from %s (streak=%d): %s", addr, consecutive_errors, exc)
                if consecutive_errors >= 5:
                    log.error("Too many decode errors from %s — closing connection", addr)
                    break
                continue

            log.debug("Reading  sensor=%s  ts=%d  value=%.2f  unit=%s",
                      reading.sensor_id, reading.timestamp, reading.value, reading.unit)

            # Persist to SQLite
            try:
                await storage.store_reading(
                    reading.sensor_id, reading.timestamp,
                    reading.value, reading.unit
                )
            except Exception as exc:
                log.error("Storage write failed for %s: %s", reading.sensor_id, exc)
                # Keep the TCP connection alive; just drop this one reading.

            # Broadcast to WebSocket clients (never blocks)
            broadcaster.publish({
                "sensor_id": reading.sensor_id,
                "timestamp": reading.timestamp,
                "value":     reading.value,
                "unit":      reading.unit,
            })

    except (asyncio.IncompleteReadError, ConnectionResetError):
        log.info("Sensor %s disconnected", addr)
    except asyncio.TimeoutError:
        log.warning("Sensor %s timed out", addr)
    except Exception as exc:
        log.exception("Unexpected error from %s: %s", addr, exc)
    finally:
        writer.close()
        try:
            await writer.wait_closed()
        except Exception:
            pass


# ═══════════════════════════════════════════════════════════════════
# Cookie helpers
# ═══════════════════════════════════════════════════════════════════

SESSION_COOKIE = "session_id"


def _ensure_session(request: web.Request, response: web.Response) -> None:
    """
    Set a session cookie on the response if the request did not include one.
    The cookie value is a random hex token; no server-side state is stored.
    """
    if SESSION_COOKIE not in request.cookies:
        token = secrets.token_hex(16)
        response.set_cookie(
            SESSION_COOKIE, token,
            httponly=True, samesite="Lax", max_age=86400 * 30
        )


